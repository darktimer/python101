# 在表示很大的数的时候，可以用下划线分割开来便于阅读
# 而且 print 打印的时候不会显示下划线
universe_age = 14_000_000_000
print(universe_age)

# 同时给多个变量赋数值，用 , 隔开，赋的值也要隔开，一一对应
x, y, z = 0, 0, 0
print('x=', x, ',y=', y, ',z=', z)

